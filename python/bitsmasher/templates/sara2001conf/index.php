<center><b><font color="#C6DFF7" face="Verdana">2001 SARA
Conference</font></b></center>
&nbsp;
<p><font color="#C6DFF7" size="-1" face="Verdana">This page
describes my trip to the Society of Amateur Radio Astronomers
(SARA) conference at the National Radio Astronomy Observatory in
Green Bank, West Virginia (NRAO Green Bank). The conference was
three days and I had a great time meeting people, going to
lectures, and using the government facilities to do real
observations.  </font></p>
<table border="1">
<tr>
<td><font color="#C6DFF7" size="-1" face="Verdana">This year was
the 20th Anniversary of the <a href="http://www.qsl.net/SARA/"
target="new">Society of Amateur Radio Astronomers</a> (SARA). We
had a meeting in <a href="https://science.nrao.edu/facilities/gbt/"
target="new">Green Bank, West Virginia</a> at the <a href=
"http://www.nrao.edu/" target="new">National Radio Astronomy
Observatory</a> (NRAO). Here is a picture of the sign at the
entrance to the facility.</font></td>
<td>
<td><a href="images/dcp_0828.jpg" onclick="swap(this); return false;"><img src="images/dcp_0828_thumb.jpg" border= "0" height="80" width="80"></a></td>
</tr>
</table>
<table border="1">
<tr>
<td><a href="images/dcp_0799.jpg"><img src="images/dcp_0799.jpg" border= "0" height="80" width="80"></a></td>
<td><font color="#C6DFF7" size="-1" face="Verdana">So what does one
do at a Radio Astronomy Conference you might ask? We spent our days
in this building, known as the <a href="Jansky.html" target=
"new">Karl Janksy</a> Laboratory, listening to lectures from
professional astronomers and physicists, and enjoying presentations
from the members of the club.</font></td>
</tr>
</table>
<table border="1">
<tr>
<td><font color="#C6DFF7" size="-1" face="Verdana">In this picture,
you can see the dormitories on the left and the cafeteria on the
right. You can also see the row of bicycles near the dorm entrance.
These were provided as a courtesy and our main mode of
transportation around the site!</font></td>
<td><a href="images/dcp_0793.jpg"><img src="images/dcp_0793.jpg" border=
"0" height="80" width="80"></a></td>
</tr>
</table>
<table border="1">
<tr>
<td><a href="images/dcp_0808.jpg"><img src="images/dcp_0808.jpg" border=
"0" height="80" width="80"></a></td>
<td><font color="#C6DFF7" size="-1" face="Verdana">On the second
floor of the dormitories above the entrance, we have the <a href=
"http://www.activemind.com/Mysterious/Topics/SETI/biography_drake.html"
target="new">Frank Drake</a> lounge. Where you see a fireplace in
this picture now, there used to be a chalk board. It is said that
this is where he introduced the now famous <a href=
"http://www.activemind.com/Mysterious/Topics/SETI/drake_equation.html"
target="new">Drake Equation</a>. This room also has a small
kitchen, library, and the obligatory <a href=
"images/dcp_0809.jpg">chalkboard full of equations</a>.</font></td>
</tr>
</table>
<table border="1">
<tr>
<td><font color="#C6DFF7" size="-1" face="Verdana">The Green Bank
staff were extremely accomodating to our club. One nice thing they
did was to turn over some of their older surplus equipment to our
club for our projects! We drew names out of a hat to decide who got
what. Here we see everyone perusing the boxes to see what they
might grab in the raffle.</font></td>
<td><a href="images/dcp_0830.jpg"><img src="images/dcp_0830.jpg" border=
"0" height="80" width="80"></a></td>
</tr>
</table>
<table border="1">
<tr>
<td><a href="images/dcp_0832.jpg"><img src="images/dcp_0832.jpg" border=
"0" height="80" width="80"></a></td>
<td><font color="#C6DFF7" size="-1" face="Verdana">Oh boy, more
lectures! Can you believe the good fortune? Here we are listening
to a talk by Gary Anderson on the calibration of the 327 mHz feed
for the 100 meter Green Bank Telescope.</font></td>
</tr>
</table>
<table border="1">
<tr>
<td><font color="#C6DFF7" size="-1" face="Verdana">Radio Telescopes
are extremely sensitive instruments. The spark plugs from the
combustion engine in an automobile can be disruptive to the
receivers that make up the "back end" of the scope. For this
reason, cars are not allowed near the dishes.</font></td>
<td><a href="images/dcp_0814.jpg"><img src="images/dcp_0814.jpg" border=
"0" height="80" width="80"></a></td>
</tr>
</table>
