<h2>Notes For Admins</h2>
<p>
When a player joins the server, they have almost no permissions. This is to prevent griefing and
such. You will have to move them from "default" group to the "player" group once you are sure they
are not insane, griefing, etc. 
</p>
The command to change someone to player is: 
<br />
/manuadd &lt;playername&gt; player 
<br />
You can also demote a griefer like so:
<br />
/manuadd &lt;playername&gt; default

